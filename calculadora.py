def soma(x,y):
    """
    param x = primeiro numero
    param y = segundo numero
    faze a soma entre x e y
    ex: soma(1,2)
    = 3
    """
    return x+y

def sub(x,y):
    return x-y

def mult(x,y):
    return x*y

def div(x,y):
    return x/y

